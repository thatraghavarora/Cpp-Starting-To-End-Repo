#include <iostream>

using namespace std;
int main() {

    // Charcater To Number
    cout<<"Convecting The  Character To Number "<<endl;
    char c = 'A';
    int ascii = (int)c;
    cout << ascii << endl;     // Output: 65

    // Number to Charater
    cout<<"Convecting The Number to Character"<<endl;
    int num = 66;
    char ch = (char)num;
    cout << ch << endl;        // Output: B


}